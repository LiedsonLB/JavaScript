// Operadores Logicos
/*
    AND - &&
    OR - ||
*/

var x = 1;
var y = 2;

// AND
console.log(x == 1 && y == 2) // true
console.log(x == 2 && y == 2) // false
console.log(x == 1 && y == 1) // false

// OR
console.log(x == 1 || y == 2) // true
console.log(x == 1 || y == 1) // true
console.log(x == 2 || y == 2) // true
console.log(x == 2 && y == 1) // false