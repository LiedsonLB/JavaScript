// Tipos de variaveis

// var - indica que é uma variavel (O valor pode ser alterado, "vária")
var variavel = "nome";

// const - indica que é uma constante (O valor pode NÃO ser alterado, ou seja "constante")
const constante  = "nome";

/* let - 
- é uma alternativa do var utilizada frequentemente em escopos como 
condicionais e loops onde somente são visíveis
- não podem ser içadas, ou seja não podem ser acessadas antes de sua declaração no código
*/
let sla  = "nome";